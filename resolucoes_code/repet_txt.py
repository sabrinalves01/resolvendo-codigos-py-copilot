# Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.

# Resolução

# Solicita uma string e um número inteiro como entrada
texto = input("Digite uma string: ")
numero_repeticoes = int(input("Digite um número inteiro: "))

# Retorna a string repetida o número de vezes informado
texto_repetido = texto * numero_repeticoes

# Exibe a string repetida
print("String repetida:", texto_repetido)
